def fmt_group(m, sq):
    g0 = m.groups()[0]
    if g0.startswith(("", "", "")):
        return g0
    if g0:
        return autof(sq)(g0)
    else:
        return autof(sq + seq.CYAN)(m.groups()[1])


def fmt_header(g2, _fmt):
    if not g2:
        return ""
    if g2 and g2.strip() in [
        "PANE",
        "PANES",
        "WINDOW",
        "STATUSBAR",
        "LAYOUT",
        "SESSION",
        "SIGNATURE",
    ]:
        return autof(seq.HI_CYAN)(g2)
    if g2 and g2.strip() in [
        "HEAD",
        "PRFIX",
        "MODIF.",
        "ARROW",
        "BINDING",
        "KEY",
    ]:
        return g2
    return _fmt(g2)


